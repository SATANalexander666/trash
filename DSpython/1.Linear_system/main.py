import numpy as np

def main():
    A = np.array([[-8, 0, 0, 0, 0], [4, 4, 0, 0, 0,], 
        [4, 1, 9, 0, 0], [7, 7, 1, 1, 0], [1, -7, 4, 2, -6]])   #Матрица коэффицентов
    B = np.array([-56, 8, 77, 12, 62])  #Столбец свободных членов
    S = np.linalg.solve(A, B)   #Столбец занятых членов (функция numpy для решения СЛАУ)
    print(S)

main()









    